# ozon/services/competitors_analyzer.py
import pandas as pd
import numpy as np
from datetime import datetime
from openpyxl import load_workbook
from openpyxl.styles import Font, Alignment, PatternFill, Border, Side
from openpyxl.utils import get_column_letter
from openpyxl.comments import Comment
from io import BytesIO

import logging

logger = logging.getLogger(__name__)

def process_ozon_data(input_file):
    """Обрабатывает файл с данными о конкурентах и возвращает содержимое Excel-файла как bytes"""
    logger.info(f"Чтение файла: {input_file}")
    
    # Сначала прочитаем без заголовков, чтобы увидеть данные
    df_raw = pd.read_excel(input_file, header=None)
    
    # Находим индекс строки с заголовками (обычно это строка, где есть "Название товара")
    header_row = None
    for i in range(min(10, df_raw.shape[0])):
        if 'Название товара' in str(df_raw.iloc[i].values):
            header_row = i
            break
    
    if header_row is None:
        raise ValueError("Не найдена строка с заголовками колонок")
    
    logger.info(f"Найдена строка с заголовками: {header_row}")
    
    # Читаем файл заново, используя найденную строку как заголовок
    df = pd.read_excel(input_file, header=header_row)
    
    logger.info(f"Успешно прочитан файл. Количество строк: {df.shape[0]}, колонок: {df.shape[1]}")
    
    # Добавление новых колонок с расчетами
    try:
        # Количество добавлений в корзину, шт
        df['Количество добавлений в корзину, шт'] = np.where(
            (df['Просмотры карточки'].notna()) & (df['В корзину из карточки, %'].notna()),
            df['Просмотры карточки'] * df['В корзину из карточки, %'] / 100,
            0
        )
        
        # Конверсия из корзины в заказ, %
        df['Конверсия из корзины в заказ, %'] = np.where(
            (df['Заказано, штуки'].notna()) & (df['Количество добавлений в корзину, шт'].notna()) & (df['Количество добавлений в корзину, шт'] != 0),
            df['Заказано, штуки'] / df['Количество добавлений в корзину, шт'] * 100,
            0
        )
        
        # Кол-во корзин для одого заказа, шт
        df['Кол-во корзин для одого заказа, шт'] = np.where(
            (df['Конверсия из корзины в заказ, %'].notna()) & (df['Конверсия из корзины в заказ, %'] != 0),
            100 / df['Конверсия из корзины в заказ, %'],
            0
        )
        
        # Стоимость корзины для ДРР 15%, ₽
        df['Стоимость корзины для ДРР 15%, ₽'] = np.where(
            (df['Средняя цена, ₽'].notna()) & (df['Кол-во корзин для одого заказа, шт'].notna()) & (df['Кол-во корзин для одого заказа, шт'] != 0),
            (df['Средняя цена, ₽'] / 100 * 15) / df['Кол-во корзин для одого заказа, шт'],
            0
        )
        
        # CTR, %
        df['CTR, %'] = np.where(
            (df['Просмотры карточки'].notna()) & (df['Показы всего'].notna()) & (df['Показы всего'] != 0),
            df['Просмотры карточки'] / df['Показы всего'] * 100,
            0
        )
        
        # Кликов для одного заказа, шт
        df['Кликов для одного заказа, шт'] = np.where(
            (df['Просмотры карточки'].notna()) & (df['Заказано, штуки'].notna()) & (df['Заказано, штуки'] != 0),
            df['Просмотры карточки'] / df['Заказано, штуки'],
            0
        )
        
        # Показов для одного заказа, шт
        df['Показов для одного заказа, шт'] = np.where(
            (df['Показы всего'].notna()) & (df['Заказано, штуки'].notna()) & (df['Заказано, штуки'] != 0),
            df['Показы всего'] / df['Заказано, штуки'],
            0
        )
    except KeyError as e:
        logger.error(f"Ошибка при обращении к колонке: {e}")
        logger.error("Доступные колонки: %s", df.columns.tolist())
        raise
    
    # Округление числовых результатов для лучшей читаемости
    numeric_columns = [
        'Количество добавлений в корзину, шт',
        'Конверсия из корзины в заказ, %',
        'Кол-во корзин для одого заказа, шт',
        'Стоимость корзины для ДРР 15%, ₽',
        'CTR, %',
        'Кликов для одного заказа, шт',
        'Показов для одного заказа, шт'
    ]
    
    for col in numeric_columns:
        if col in df.columns:
            df[col] = df[col].round(1)
    
    # Создаем буфер в памяти для сохранения файла Excel
    output = BytesIO()
    
    # Сохраняем результат без форматирования
    df.to_excel(output, index=False)
    
    # Применяем форматирование к файлу Excel
    output.seek(0)
    wb = load_workbook(output)
    ws = wb.active
    
    # Словарь с комментариями к колонкам
    column_comments = {
        'Название товара': 'Название товара из выгрузки OZON',
        'Ссылка на товар': 'Ссылка на карточку товара на OZON',
        'Продавец': 'Название продавца товара',
        'Бренд': 'Название бренда товара',
        'Категория 1 уровня': 'Основная категория товара',
        'Категория 3 уровня': 'Подкатегория товара',
        'Признак товара': 'Дополнительная информация о товаре',
        'Заказано на сумму, ₽': 'Общая сумма заказов в рублях',
        'Динамика оборота, %': 'Изменение оборота в процентах',
        'Заказано, штуки': 'Количество заказанных единиц товара',
        'Средняя цена, ₽': 'Средняя цена товара в рублях',
        'Минимальная цена, ₽': 'Минимальная цена товара в рублях',
        'Доля выкупа, %': 'Процент выкупленных товаров из заказанных',
        'Упущенные продажи, ₽': 'Оценка упущенных продаж в рублях',
        'Доступность за 28 дней, %': 'Процент времени, когда товар был доступен за 28 дней',
        'Индекс локализации за 14 дней, %': 'Показатель локализации за 14 дней в процентах',
        'Среднесуточные продажи, ₽': 'Средние продажи за сутки в рублях',
        'Среднесуточные продажи, штуки': 'Среднее количество проданных единиц за сутки',
        'Остаток на конец периода, штуки': 'Количество единиц товара на складе в конце периода',
        'Схема работы': 'Схема работы с товаром (FBO, FBS и т.д.)',
        'Сроки доставки, дни': 'Срок доставки товара в днях',
        'Объем товара, л': 'Объем товара в литрах',
        'Показы всего': 'Общее количество показов товара',
        'Просмотры в поиске и каталоге': 'Количество просмотров в поиске и каталоге',
        'Просмотры карточки': 'Количество просмотров карточки товара',
        'Конверсия из показа в заказ, %': 'Процент конверсии из показа в заказ',
        'В корзину из поиска и каталога, %': 'Процент добавлений в корзину из поиска и каталога',
        'В корзину из карточки, %': 'Процент добавлений в корзину из карточки товара',
        'Скидка за счет акций': 'Размер скидки за счет акций',
        'Доля оборота в акциях, %': 'Процент оборота в акциях',
        'Дней в акциях': 'Количество дней участия в акциях',
        'Дней с продвижением': 'Количество дней с продвижением',
        'Доля рекламных расходов, %': 'Процент расходов на рекламу',
        'Дата создания карточки товара': 'Дата создания карточки товара на OZON',
        
        # Добавленные расчетные колонки
        'Количество добавлений в корзину, шт': 'Рассчитано как: Просмотры карточки * В корзину из карточки, % / 100',
        'Конверсия из корзины в заказ, %': 'Рассчитано как: Заказано, штуки / Количество добавлений в корзину, шт * 100',
        'Кол-во корзин для одого заказа, шт': 'Рассчитано как: 100 / Конверсия из корзины в заказ, %',
        'Стоимость корзины для ДРР 15%, ₽': 'Рассчитано как: (Средняя цена, ₽ / 100 * 15) / Кол-во корзин для одого заказа, шт',
        'CTR, %': 'Рассчитано как: Просмотры карточки / Показы всего * 100',
        'Кликов для одного заказа, шт': 'Рассчитано как: Просмотры карточки / Заказано, штуки',
        'Показов для одного заказа, шт': 'Рассчитано как: Показы всего / Заказано, штуки'
    }
    
    # Стили для форматирования
    header_font = Font(bold=True, size=11, color="FFFFFF")
    header_fill = PatternFill(start_color="44546A", end_color="44546A", fill_type="solid")
    header_alignment = Alignment(horizontal='center', vertical='center', wrap_text=True)
    
    # Стиль для границ
    thin_border = Border(
        left=Side(style='thin'),
        right=Side(style='thin'),
        top=Side(style='thin'),
        bottom=Side(style='thin')
    )
    
    # Стиль для основных данных
    data_alignment = Alignment(horizontal='center', vertical='center')
    
    # Стиль для расчетных колонок
    calculated_fill = PatternFill(start_color="E2EFDA", end_color="E2EFDA", fill_type="solid")
    
    # Форматирование заголовков
    for col in range(1, ws.max_column + 1):
        cell = ws.cell(row=1, column=col)
        cell.font = header_font
        cell.fill = header_fill
        cell.alignment = header_alignment
        cell.border = thin_border
        
        # Добавление комментариев к заголовкам
        column_name = cell.value
        if column_name in column_comments:
            cell.comment = Comment(column_comments[column_name], "Описание")
    
    # Автоподбор ширины колонок
    for col in range(1, ws.max_column + 1):
        column_letter = get_column_letter(col)
        # Установим минимальную ширину
        ws.column_dimensions[column_letter].width = 15
        
        # Для длинных колонок увеличим ширину
        if col == 1:  # Название товара
            ws.column_dimensions[column_letter].width = 40
        elif col == 2:  # Ссылка на товар
            ws.column_dimensions[column_letter].width = 30
    
    # Определение индексов расчетных колонок
    calculated_cols = []
    for col in range(1, ws.max_column + 1):
        cell = ws.cell(row=1, column=col)
        if cell.value in ['Количество добавлений в корзину, шт', 'Конверсия из корзины в заказ, %', 
                         'Кол-во корзин для одого заказа, шт', 'Стоимость корзины для ДРР 15%, ₽', 
                         'CTR, %', 'Кликов для одного заказа, шт', 'Показов для одного заказа, шт']:
            calculated_cols.append(col)
    
    # Форматирование данных
    for row in range(2, ws.max_row + 1):
        for col in range(1, ws.max_column + 1):
            cell = ws.cell(row=row, column=col)
            cell.border = thin_border
            cell.alignment = data_alignment
            
            # Раскрасим расчетные колонки
            if col in calculated_cols:
                cell.fill = calculated_fill
    
    # Закрепление верхней строки
    ws.freeze_panes = 'A2'
    
    # Сохраняем отформатированный файл в новый буфер
    formatted_output = BytesIO()
    wb.save(formatted_output)
    formatted_output.seek(0)
    
    return formatted_output.getvalue()